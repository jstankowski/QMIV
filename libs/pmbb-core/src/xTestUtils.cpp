int64 xPerlinNoise::xLerp(int64 a, int64 b, int64 t, int32 Log2Scale)
{
  //1st pass A = a * Scale  , B = b * Scale  , T = t * Scale^3, R = r * Scale^3
  //2nd pass A = a * Scale^3, B = b * Scale^3, T = t * Scale^3, R = r * Scale^5
  int32 Add = 1 << (Log2Scale - 1);
  int64 R   = ((a << (3 * Log2Scale)) + t * (b - a) + Add) >> (Log2Scale);
  return R;
}
int32 xPerlinNoise::xFade(int64 T, int32 Log2Scale)
{
  //T = t * Scale, R = r * Scale^3
  int32 Add   = 1 << (Log2Scale - 1);
  int64 ImmR5 = (         6 * T * T * T * T * T + Add) >> Log2Scale;
  int64 ImmR4 = (ImmR5 - 15 * T * T * T * T     + Add) >> Log2Scale;
  int64 R     = (ImmR4 + 10 * T * T * T              );
  return (int32)R;
}
int32 xPerlinNoise::xDotGrad(uint32 Hash, int32 xf, int32 yf)
{
  switch(Hash & 0x7)
  {
  case 0x0: return  xf + yf;
  case 0x1: return  xf;
  case 0x2: return  xf - yf;
  case 0x3: return -yf;
  case 0x4: return -xf - yf;
  case 0x5: return -xf;
  case 0x6: return -xf + yf;
  case 0x7: return  yf;
  default:  return  0;
  }
}
int32 xPerlinNoise::PerlinPelI(uint32 CoordX, uint32 CoordY, uint32 Log2Scale, uint32 BitDepth, uint32 Seed)
{
  assert(Log2Scale <= 12);

  const int32 Scale    = xLog2SizeToSize  (Log2Scale);
  const int32 FracMask = xBitDepth2BitMask(Log2Scale);

  // Top-left coordinates of the unit-square. Wrap to range 0-255.
  uint32 IntPartX = ((CoordX >> Log2Scale) + Seed) & 0xFF;
  uint32 IntPartY = ((CoordY >> Log2Scale) + Seed) & 0xFF;

  // Input location in the unit-square.
  int32 FracPartX0 = CoordX & FracMask ; //FracPartX0 = xf0 * Scale
  int32 FracPartY0 = CoordY & FracMask ; //FracPartY0 = yf0 * Scale
  int32 FracPartX1 = FracPartX0 - Scale; //FracPartX1 = xf1 * Scale
  int32 FracPartY1 = FracPartY0 - Scale; //FracPartY1 = yf1 * Scale

  // Optionaly increase precision of calculationa
  const int32 Log2ScaleImm = Log2Scale < 8 ? 8 : Log2Scale;
  if(Log2Scale < 8)
  {
    const int32 DeltaShift = 8 - Log2Scale;
    FracPartX0 <<= DeltaShift;
    FracPartY0 <<= DeltaShift;
    FracPartX1 <<= DeltaShift;
    FracPartY1 <<= DeltaShift;
  }

  // Apply the fade function to the location.
  int32 IntU = xFade(FracPartX0, Log2ScaleImm); //IntU = u * ScaleImm^3
  int32 IntV = xFade(FracPartY0, Log2ScaleImm); //IntV = v * ScaleImm^3

  //const int64 ScaleImm = xLog2SizeToSize(Log2ScaleImm);
  //fmt::print("u={} v={}\n", (flt32)IntU / (flt32)(ScaleImm * ScaleImm * ScaleImm), (flt32)IntV / (flt32)(ScaleImm * ScaleImm * ScaleImm));

  // Generate hash values for each point of the unit-square.
  uint32 Hash00 = c_Permutation[c_Permutation[IntPartX + 0] + IntPartY + 0];
  uint32 Hash01 = c_Permutation[c_Permutation[IntPartX + 0] + IntPartY + 1];
  uint32 Hash10 = c_Permutation[c_Permutation[IntPartX + 1] + IntPartY + 0];
  uint32 Hash11 = c_Permutation[c_Permutation[IntPartX + 1] + IntPartY + 1];

  // Linearly interpolate between dot products of each gradient with its distance to the input location.
  int64 X1 = xLerp(xDotGrad(Hash00, FracPartX0, FracPartY0), xDotGrad(Hash10, FracPartX1, FracPartY0), IntU, Log2ScaleImm); //X1 = x1 * ScaleImm^3 
  int64 X2 = xLerp(xDotGrad(Hash01, FracPartX0, FracPartY1), xDotGrad(Hash11, FracPartX1, FracPartY1), IntU, Log2ScaleImm); //X2 = x2 * ScaleImm^3
  int64 R  = xLerp(X1, X2, IntV, Log2ScaleImm); //R = r * ScaleImm^5
  //fmt::print("x1={} x1={} r={} ", (flt32)X1 / (flt32)(ScaleImm * ScaleImm * ScaleImm), (flt32)X2 / (flt32)(ScaleImm * ScaleImm * ScaleImm), (flt32)R / (flt32)(ScaleImm * ScaleImm * ScaleImm * ScaleImm * ScaleImm));
  int64 RN = R >> (5 * Log2ScaleImm - (BitDepth + c_FracBits)); //RN = r * 2^(BitDepth+c_FracBits)
  //fmt::print("RN={} ", (flt32)RN / (flt32)(1<<(BitDepth + c_FracBits))); 
  return (int32)RN;
}
void xPerlinNoise::fillPerlinNoiseI(uint16* Dst, int32 DstStride, int32 Width, int32 Height, int32 BitDepth, int32 Headroom, uint32 Seed)
{
  const int32 MaxValue = (uint32)xBitDepth2MaxValue(BitDepth) - Headroom;
  const int32 MidValue = (uint32)xBitDepth2MidValue(BitDepth);

  for(int32 y = 0; y < Height; y++)
  {
    for(int32 x = 0; x < Width; x++)
    {
      int64 Sum = (xPerlinNoise::PerlinPelI(x, y, 5, BitDepth, Seed) << 2) + (xPerlinNoise::PerlinPelI(x, y, 4, BitDepth, Seed) << 1)
                +  xPerlinNoise::PerlinPelI(x, y, 3, BitDepth, Seed)       +  xPerlinNoise::PerlinPelI(x, y, 2, BitDepth, Seed);
      int64 Noise = xDivideUsingShiftSigned(Sum, xPerlinNoise::c_FracBits + 3) + MidValue;
      Dst[x] = (uint16)xClip((int32)(Noise), Headroom, MaxValue);
    }
    Dst += DstStride;
  }
}

